import pytest
from src.processing import filter_dicts_by_state
from src.processing import sort_dicts_by_date


@pytest.mark.parametrize("input_state, expected_output", [
    ('EXECUTED', [
        {'id': 41428829, 'state': 'EXECUTED', 'date': '2019-07-03T18:35:29.512364'},
        {'id': 939719570, 'state': 'EXECUTED', 'date': '2018-06-30T02:08:58.425572'}
    ]),
    ('CANCELED', [
        {'id': 594226727, 'state': 'CANCELED', 'date': '2018-09-12T21:27:25.241689'},
        {'id': 615064591, 'state': 'CANCELED', 'date': '2018-10-14T08:21:33.419441'}
    ])
])
def test_filter_dicts_by_state(sample_data, input_state, expected_output):
    result = filter_dicts_by_state(sample_data, input_state)
    assert result == expected_output


